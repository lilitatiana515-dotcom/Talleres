
print("=== RECETA: SPAGHETTI ALLA NAPOLETANA ===")
print()

print("INGREDIENTES:")
print("- 400g spaghetti")
print("- 500g tomates maduros")
print("- 2 dientes de ajo")
print("- 1 cebolla mediana")
print("- Albahaca fresca")
print("- Aceite de oliva")
print("- Sal y pimienta")
print("- Queso parmesano (opcional)")
print()

print("UTENSILIOS:")
print("- Olla grande")
print("- Sartén grande")
print("- Cuchillo")
print("- Tabla de cortar")
print("- Colador")
print()

input("Presione Enter para continuar con la preparación...")
print()


print("PREPARAR SALSA:")
paso = 1
while paso <= 7:
    if paso == 1:
        print(paso, ". Picar finamente la cebolla y el ajo")
    elif paso == 2:
        print(paso, ". Cortar los tomates en cubos pequeños")
    elif paso == 3:
        print(paso, ". Calentar 3 cucharadas de aceite en la sartén")
    elif paso == 4:
        print(paso, ". Sofreír la cebolla y ajo hasta que estén dorados")
    elif paso == 5:
        print(paso, ". Agregar los tomates picados")
    elif paso == 6:
        print(paso, ". Cocinar a fuego lento por 20 minutos")
    elif paso == 7:
        print(paso, ". Agregar sal, pimienta y albahaca picada")
    paso = paso + 1

print("--- Salsa lista ---")
print()

input("Presione Enter para continuar con la pasta...")
print()


print("COCINAR PASTA:")
paso = 1
while paso <= 5:
    if paso == 1:
        print(paso, ". Llenar la olla con agua y agregar sal")
    elif paso == 2:
        print(paso, ". Hervir el agua")
    elif paso == 3:
        print(paso, ". Agregar los spaghetti")
    elif paso == 4:
        print(paso, ". Cocinar por 8-10 minutos")
    elif paso == 5:
        print(paso, ". Escurrir la pasta")
    paso = paso + 1

print("--- Pasta lista ---")
print()

input("Presione Enter para finalizar...")
print()


print("MEZCLAR Y SERVIR:")
print("1. Agregar los spaghetti a la salsa")
